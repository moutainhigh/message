package com.zhongan.icare.message.push.enums;

public enum MsgStatusEnum {
    READED(1, "已读"),
    UNREAD(0, "未读");
    private int    code;
    private String des;

    private MsgStatusEnum(int code, String des) {
        this.code = code;
        this.des = des;
    }

    public int getCode() {
        return code;
    }

    public String getDes() {
        return des;
    }

}
