package com.zhongan.icare.message.push.web.dto;

public class SingleMsgPushDTO {

    private Integer messageType;

    private Integer messageLayout;

    private String  messageTitle;

    private String  messageDetail;

    private String  targetMobile;

    public Integer getMessageType() {
        return messageType;
    }

    public void setMessageType(Integer messageType) {
        this.messageType = messageType;
    }

    public Integer getMessageLayout() {
        return messageLayout;
    }

    public void setMessageLayout(Integer messageLayout) {
        this.messageLayout = messageLayout;
    }

    public String getMessageTitle() {
        return messageTitle;
    }

    public void setMessageTitle(String messageTitle) {
        this.messageTitle = messageTitle;
    }

    public String getMessageDetail() {
        return messageDetail;
    }

    public void setMessageDetail(String messageDetail) {
        this.messageDetail = messageDetail;
    }

    public String getTargetMobile() {
        return targetMobile;
    }

    public void setTargetMobile(String targetMobile) {
        this.targetMobile = targetMobile;
    }

}
